function getDailyItem(rank)
--Determine the item of the day, based on rank.
timer = os.time();
counter = -1;
while (timer > 1009810800) do
	timer = (timer-86400);
	counter = counter+1;
end
--print("Days:",counter);
--print("Rank for today:",counter%rank);
i = 1;
j = 0;
for i=1,(table.getn(cookingDB)/6) do
	if (cookingDB[(i*6)-5] == (counter%(rank+1))) then
		j=j+1;
		--print(i,j,cookingDB[(i*6)-4]);
	elseif (cookingDB[(i*6)-5] > (counter%(rank+1))) then
		k = i-1;
		--print("Breaking...",i,j,k);
		break;
	end
end
--print(k,(k*6)-4,(counter%j));
item = cookingDB[((k-(counter%j))*6)-4];
hqitem = cookingDB[((k-(counter%j))*6)-3];
nqpoints = cookingDB[((k-(counter%j))*6)-2];
hqpoints = cookingDB[((k-(counter%j))*6)-1];
value = cookingDB[((k-(counter%j))*6)];
return item,hqitem,nqpoints,hqpoints,value;
end;